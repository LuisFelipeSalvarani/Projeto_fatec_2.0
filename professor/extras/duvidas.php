            <h2>Como trocar sua senha?</h2>
            <p>Na tela de login você encontrara a opção de <b>"Esqueceu sua senha?"</b> , clique nesta opção e então
                digite o mesmo e-mail que foi utilizado no seu cadastro, e então vá até seu e-mail e verifique por um
                e-mail da Fatec, onde você terá um link. Acessando esse link você definira sua nova senha e confirmá-la
                corretamente. Lembre-se de criar uma senha forte para a sua segurança.</p>
        </article>
        <article class="texto reveal">
            <h2>Como se candidatar em um edital?</h2>
            <p>Na página inicial do nosso portal, estarão disponíveis todos os nossos cursos da Fatec Itapira. Para
                participar de um edital basta escolher o curso, acessar a página de editais deste curso através dos
                cards que estão disponíveis na página e então ser direcionado para página de editais referente ao curso
                escolhido. Leia as informações e requisitos sobre o edital e então clique em Participar <b>(lembrando
                    que é necessário estar logado para isso)</b>, sendo redirecionado para uma página onde o candidato
                deverá confirmar seus dados e encaminhar uma série de documentos, e então aguardar um e-mail de
                confirmação da coordenação.</p>
        </article>
        <article class="texto reveal">
            <h2>Como vou saber se estou inscrito em um edital?</h2>
            <p>Para confirmar sua inscrição a coordenação da Fatec irá enviar um e-mail, informando sobre o deferimento
                da sua inscrição. No caso de indeferimento, também será enviado um e-mail com o motivo do candidato não
                poder participar do processo seletivo.</p>
