            <h2>inauguração</h2>
            <p>A Fatec de Itapira “Ogari de Castro Pacheco” foi inaugurada no dia 28 de junho de 2014, por meio de um
                convênio entre Prefeitura Municipal de Itapira e o Centro Estadual de Educação Tecnológica Paula Souza.
                A parceria para implantação da unidade resultou na reforma e adequação das instalações físicas da antiga
                sede da escola SESI, localizada à rua Tereza Lera Paoletti, 590, no Jardim Bela Vista. Como patrono, a
                faculdade tem a figura do empresário itapirense Dr. Ogari de Castro Pacheco, presidente e fundador do
                Laboratório Cristália.</p>
        </article>
        <article class="texto reveal">
            <h2>o primeiro semestre da fatec itapira</h2>
            <p>As aulas do primeiro semestre da unidade escolar iniciaram no dia 21 de julho de 2014, com o CST em
                Gestão da Tecnologia da Informação e seis meses depois, no primeiro semestre de 2015, foi instalado o
                Curso Superior de Tecnologia em Gestão da Produção Industrial. No segundo semestre de 2017, a Fatec de
                Itapira passou a oferecer o Curso Superior de Tecnologia em Gestão Empresarial. Atenta as mudanças do
                mercado de trabalho a Fatec de Itapira incluiu em seu portfólio o Curso Superior de Tecnologia em
                Desenvolvimento de Software Multiplataforma, no primeiro semestre de 2022.</p>
        </article>
        <article class="texto reveal">
            <h2>missão</h2>
            <p>Promover a educação superior profissional pública dentro de referenciais de excelência visando o
                atendimento das demandas sociais e do mundo profissional.</p>
        </article>
        <article class="texto reveal">
            <h2>visão</h2>
            <p>Consolidar-se como Instituição de Ensino Superior de excelência e estimular o desenvolvimento humano e
                tecnológico adaptado à necessidades da sociedade em suas diversas áreas de atuação.</p>
        </article>
        <article class="texto reveal">
            <h2>valores</h2>
